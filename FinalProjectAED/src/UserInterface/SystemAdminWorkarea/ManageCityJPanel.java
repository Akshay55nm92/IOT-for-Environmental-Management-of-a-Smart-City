    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        String name = nameJTextField.getText();

        if (!system.checkIfCityNameisUnique(name)) {
            if (!name.isEmpty()) {
                Network network = system.createAndAddNetwork();
                network.setName(name);
                populateCityTable();

            } else {
                JOptionPane.showMessageDialog(null, "City Should have a name.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Not a unique City Name");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

    private void btnDeleteNetworkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteNetworkActionPerformed
        // TODO add your handling code here:
        int selectedRow = cityJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select the row");
            return;
        }
        Network net = (Network) cityJTable.getValueAt(selectedRow, 0);
        system.DeleteCity(net);
        populateCityTable();
    }//GEN-LAST:event_btnDeleteNetworkActionPerformed

